package com.nforum.platform.util;

public class KeyValue {

	String key;
	String value;
	
	public KeyValue(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}

	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}

	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	
	
	
	
}
